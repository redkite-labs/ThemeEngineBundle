namespace AlphaLemon\ThemeEngineBundle\DependencyInjection\Compiler;

use Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use PageTreeCore\Compiler\ListenersRegistrator;

class RegisterThemeEngineListenersPass implements CompilerPassInterface
{
    public function process(ContainerBuilder $container)
    {
        ListenersRegistrator::registerByTaggedServiceId($container, 'alfrontend.event_listener');
    }
}

